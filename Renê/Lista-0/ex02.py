# Leia duas variáveis do tipo int e realize a soma entre elas. Imprima o resultado.

x1 = int(input('Insira o 1° número: '))
x2 = int(input('Insira o 2° número: '))

resultado = x1 + x2

print(f'O resultado da soma de {x1} e {x2} é igual a {resultado}.')

